# Another question made with an external HTML designer (html5-editor.com is better)
# This one doesn't have variants

import d2l
from TruthTableHTML.html_tt import html_tt

pool = d2l.QuestionPool("Magnitude Comparator")

with open("yapping_2.html", "r") as f:
    q_text = f.read()

headers = ["GT_in", "LT_in", "a", "b", "GT_out", "LT_out"]
correct_tt_cols = ["001000001111xxxx", "010011110000xxxx"]
distractors = [
    ["010000001111xxxx", "001011110000xxxx"],
    ["001011110000xxxx", "010000001111xxxx"],
    ["100100001111xxxx", "100111110000xxxx"]
]

question = d2l.MCQuestion(q_text)

# Right answer
table = html_tt(correct_tt_cols, headers)
question.add_answer(table, 100)

# Wrong answers
for i in range(3):
    table = html_tt(distractors[i], headers)
    question.add_answer(table, 0)

pool.add_question(question)
pool.package()











